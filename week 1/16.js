//simple calculator

 let num1= 4;
 let num2= 4;
 let operator = '%';
 
 if(operator == '+'){
   console.log(num1+num2) ;
 }
 else if(operator=='-'){
    console.log(num1-num2);
 }
 else if(operator=='*'){
    console.log(num1*num2);
 }
 else if(operator=='/'){
    console.log(num1/num2);
 }
 else if(operator=='%'){
    console.log(num1%num2);
 }

